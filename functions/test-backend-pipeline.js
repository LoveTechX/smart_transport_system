/**
 * Automated emulator integration tests for backend pipeline triggers.
 *
 * Prerequisites:
 *   - Firestore emulator running at 127.0.0.1:8080
 *   - Functions emulator running at 127.0.0.1:5001
 *
 * Run:
 *   cd functions
 *   node test-backend-pipeline.js
 */

'use strict';

process.env.FIRESTORE_EMULATOR_HOST = "127.0.0.1:8080";
process.env.GCLOUD_PROJECT = "demo-no-project";

const admin = require("firebase-admin");
const { FieldValue } = require("firebase-admin/firestore");

admin.initializeApp({
    projectId: "demo-no-project"
});

const db = admin.firestore();

const POLL_INTERVAL_MS = 500;
const POLL_TIMEOUT_MS = 10_000;

function sleep(ms) {
    return new Promise((resolve) => setTimeout(resolve, ms));
}

function assert(condition, message) {
    if (!condition) {
        throw new Error(message);
    }
}

async function pollUntil(label, checkFn, timeoutMs = POLL_TIMEOUT_MS, intervalMs = POLL_INTERVAL_MS) {
    const start = Date.now();

    while (Date.now() - start <= timeoutMs) {
        const result = await checkFn();
        if (result) {
            return result;
        }
        await sleep(intervalMs);
    }

    throw new Error(`Timeout waiting for ${label} after ${timeoutMs}ms`);
}

async function safeDelete(ref) {
    const snap = await ref.get();
    if (snap.exists) {
        await ref.delete();
    }
}

async function deleteQueryDocs(query) {
    const snap = await query.get();
    if (snap.empty) {
        return;
    }

    const batch = db.batch();
    snap.docs.forEach((doc) => batch.delete(doc.ref));
    await batch.commit();
}

async function runStage(stageName, stageFn) {
    try {
        await stageFn();
        console.log(`✔ SUCCESS ${stageName}`);
    } catch (error) {
        const message = error && error.message ? error.message : String(error);
        throw new Error(`${stageName}: ${message}`);
    }
}

async function testTicketVerification() {
    const tripId = 'tripPipelineTicket';
    const ticketId = 'ticketPipelineTicket';

    const tripRef = db.collection('trips').doc(tripId);
    const ticketRef = db.collection('tickets').doc(ticketId);

    await safeDelete(ticketRef);
    await safeDelete(tripRef);

    await tripRef.set({
        vehicleId: 'bus101',
        routeId: 'route1',
        currentOccupancy: 0,
        availableSeats: 40,
        verifiedTicketCount: 0,
        seatMap: {}
    });

    await ticketRef.set({
        tripId,
        routeId: 'route1',
        vehicleId: 'bus101',
        seatNumber: '12',
        status: 'pending'
    });

    await ticketRef.update({ status: 'verified' });

    const tripSnap = await pollUntil(`trips/${tripId} verification update`, async () => {
        const snap = await tripRef.get();
        if (snap.exists && snap.data()?.verifiedTicketCount === 1) {
            return snap;
        }
        return null;
    });

    const trip = tripSnap.data();

    assert(trip.verifiedTicketCount === 1, `verifiedTicketCount expected 1, got ${trip.verifiedTicketCount}`);
    assert(trip.currentOccupancy === 1, `currentOccupancy expected 1, got ${trip.currentOccupancy}`);
    assert(trip.availableSeats === 39, `availableSeats expected 39, got ${trip.availableSeats}`);
    assert(trip.seatMap && trip.seatMap['12'], 'seatMap not updated for seat 12');
}

async function testTelemetryProcessing() {
    const vehicleId = 'bus101';
    const telemetryRef = db.collection('telemetry').doc(vehicleId);
    const requiredAlertRef = db.collection('alerts').doc('bus101_overspeed');

    await safeDelete(telemetryRef);
    await safeDelete(requiredAlertRef);
    await deleteQueryDocs(
        db.collection('alerts')
            .where('vehicleId', '==', vehicleId)
            .where('type', '==', 'overspeed')
    );

    await telemetryRef.set({
        vehicleId,
        latitude: 30.7333,
        longitude: 76.7794,
        speedKmh: 95,
        heading: 120,
        updatedAt: FieldValue.serverTimestamp()
    });

    const alertData = await pollUntil('alerts/bus101_overspeed', async () => {
        const requiredSnap = await requiredAlertRef.get();
        if (requiredSnap.exists) {
            return requiredSnap.data();
        }

        // Backward-compatible fallback for alert IDs generated by timestamped patterns.
        const querySnap = await db
            .collection('alerts')
            .where('vehicleId', '==', vehicleId)
            .where('type', '==', 'overspeed')
            .limit(1)
            .get();

        if (!querySnap.empty) {
            return querySnap.docs[0].data();
        }

        return null;
    });

    assert(alertData.type === 'overspeed', `alert type expected overspeed, got ${alertData.type}`);
}

async function testEtaPrediction() {
    const vehicleId = 'bus102';
    const routeId = 'route_test_pipeline';
    const tripId = 'trip_pipeline_test';

    const routeRef = db.collection('routes').doc(routeId);
    const tripRef = db.collection('trips').doc(tripId);
    const telemetryRef = db.collection('telemetry').doc(vehicleId);
    const etaRef = db.collection('etas').doc(vehicleId);

    await safeDelete(telemetryRef);
    await safeDelete(etaRef);
    await safeDelete(tripRef);
    await safeDelete(routeRef);

    await routeRef.set({
        routeId,
        stops: [
            { stopId: 'central_station', latitude: 28.6139, longitude: 77.2090, sequence: 1 },
            { stopId: 'rajiv_chowk', latitude: 28.6328, longitude: 77.2197, sequence: 2 },
            { stopId: 'khan_market', latitude: 28.6005, longitude: 77.2270, sequence: 3 },
            { stopId: 'airport_terminal', latitude: 28.5562, longitude: 77.1000, sequence: 4 }
        ]
    });

    await tripRef.set({
        tripId,
        vehicleId,
        routeId
    });

    await telemetryRef.set({
        vehicleId,
        latitude: 30.7333,
        longitude: 76.7794,
        speedKmh: 40,
        updatedAt: FieldValue.serverTimestamp()
    });

    const etaData = await pollUntil('etas/bus102', async () => {
        const snap = await etaRef.get();
        return snap.exists ? snap.data() : null;
    });

    assert(etaData.nextStopId !== undefined && etaData.nextStopId !== null, 'nextStopId is missing in etas/bus102');
    assert(
        etaData.predictedArrival !== undefined && etaData.predictedArrival !== null,
        'predictedArrival is missing in etas/bus102'
    );
}

async function testAnalyticsAggregation() {
    const tripId = 'tripAnalyticsTest';
    const tripRef = db.collection('trips').doc(tripId);
    const analyticsRef = db.collection('analytics').doc('route1');

    await safeDelete(tripRef);
    await safeDelete(analyticsRef);

    await tripRef.set({
        vehicleId: 'bus101',
        routeId: 'route1',
        currentOccupancy: 15,
        availableSeats: 25,
        verifiedTicketCount: 15,
        seatMap: {}
    });

    const analyticsData = await pollUntil('analytics/route1', async () => {
        const snap = await analyticsRef.get();
        return snap.exists ? snap.data() : null;
    });

    assert(
        analyticsData.averageOccupancy !== undefined && analyticsData.averageOccupancy !== null,
        'averageOccupancy is missing in analytics/route1'
    );
    assert(
        analyticsData.utilization !== undefined && analyticsData.utilization !== null,
        'utilization is missing in analytics/route1'
    );
}

async function main() {
    await runStage('Ticket Verification', testTicketVerification);
    await runStage('Telemetry Processing', testTelemetryProcessing);
    await runStage('ETA Prediction', testEtaPrediction);
    await runStage('Analytics Aggregation', testAnalyticsAggregation);

    console.log('ALL BACKEND PIPELINE TESTS PASSED');
}

main()
    .catch((error) => {
        const message = error && error.message ? error.message : String(error);
        console.error(`✖ FAILURE ${message}`);
        process.exitCode = 1;
    })
