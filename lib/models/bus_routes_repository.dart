  static List<RouteStop> _createStops(
    String routeId,
    List<(String name, double lat, double lon, int minutes)> stops,
  ) {
    return stops.asMap().entries.map((e) {
      final (name, lat, lon, minutes) = e.value;
      return RouteStop(
        id: '${routeId}_stop_${e.key}',
        stopName: name,
        latitude: lat,
        longitude: lon,
        sequenceNumber: e.key,
        arrivalMinutes: minutes,
      );
    }).toList();
  }

  static BusRoute? getRouteById(String routeId) {
    try {
      return allRoutes.firstWhere((r) => r.id == routeId);
    } catch (_) {
      return null;
    }
  }

  static List<BusRoute> searchRoutes(String query) {
    final q = query.toLowerCase();
    return allRoutes
        .where((r) =>
            r.routeNumber.contains(q) ||
            r.name.toLowerCase().contains(q) ||
            r.source.toLowerCase().contains(q) ||
            r.destination.toLowerCase().contains(q))
        .toList();
  }
